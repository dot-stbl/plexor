function getIcon(coll, name) {
  let ic = coll.icons[name];
  if (!ic && coll.aliases && coll.aliases[name]) ic = coll.icons[coll.aliases[name].parent];
  if (!ic) throw new Error('missing icon: ' + name);
  return { body: ic.body, width: ic.width ?? coll.width ?? 24, height: ic.height ?? coll.height ?? 24 };
}

