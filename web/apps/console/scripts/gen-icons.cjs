const HEAD = "// GENERATED by scripts/gen-icons.cjs — do not edit by hand.\n// Regenerate after changing the icon set. Data inlined from @iconify-json.\n";

let icon = HEAD +
  "import { Icon as Iconify, type IconifyIcon } from '@iconify/react';\n" +
  "import type { SVGProps, ReactElement } from 'react';\n\n" +
  "/**\n * Central icon module — Google Material Symbols Rounded (Iconify data,\n" +
  " * inlined offline). Import UI icons ONLY from here. Swap vendor = regen.\n *\n" +
  " * Components accept SVG-like props (className, strokeWidth, data-*, aria-*)\n" +
  " * for drop-in Phosphor compatibility; props are forwarded to @iconify/react\n" +
  " * (strokeWidth is a no-op — Material Symbols are filled, not stroked).\n */\n" +
  "export type IconProps = SVGProps<SVGSVGElement>;\n" +
  "export type Icon = (props: IconProps) => ReactElement;\n\n" +
  "const mk = (data: IconifyIcon): Icon =>\n  function IconCmp(props: IconProps) {\n    return <Iconify icon={data} {...(props as Record<string, unknown>)} />;\n  };\n\n";
