/**
 * gen-tech-icons.cjs — emits src/shared/ui/tech-icon-data.ts.
 *
 * UI icons are no longer inlined here; they come from
 * @nine-thirty-five/material-symbols-react (tree-shakeable, per-style/weight
 * subpaths). This script only generates the BRAND LOGO data (postgres, redis,
 * k8s, etc.) consumed by the <TechIcon> primitive — see web-frontend.md rule
 * 63 ("Цветные бренд/тех-логотипы — отдельно через `<TechIcon>` (Iconify
 * `logos:`)").
 *
 * Aliases (e.g. `postgres`/`postgresql`, `kubernetes`/`k8s`) reference the
 * SAME data const — the SVG blob is stored once per unique logo, not once per
 * slug. To add a new alias: append `slug: 'iconify-name'` to LOG below and
 * re-run.
 *
 * Run from apps/console/: `bun run gen:tech-icons`.
 */

const fs = require('fs');
const path = require('path');
const lg = require(path.resolve('node_modules/@iconify-json/logos/icons.json'));

function getIcon(coll, name) {
  let ic = coll.icons[name];
  if (!ic && coll.aliases && coll.aliases[name]) ic = coll.icons[coll.aliases[name].parent];
  if (!ic) throw new Error('missing logo: ' + name);
  return { body: ic.body, width: ic.width ?? coll.width ?? 24, height: ic.height ?? coll.height ?? 24 };
}

// slug → iconify name. Multiple slugs may point at the same iconify name
// (alias); the generator dedupes the SVG data automatically.
const LOG = {
  postgres: 'postgresql',
  postgresql: 'postgresql',
  redis: 'redis',
  kafka: 'kafka',
  ubuntu: 'ubuntu',
  debian: 'debian',
  docker: 'docker',
  kubernetes: 'kubernetes',
  k8s: 'kubernetes',
  nginx: 'nginx',
  rocky: 'rocky-linux',
};

const HEAD =
  '// GENERATED by scripts/gen-tech-icons.cjs — do not edit by hand.\n' +
  '// Regenerate after changing the logo set. Data inlined from @iconify-json/logos.\n' +
  '// Aliases share one data const per unique logo (no SVG duplication).\n';

// Group slugs by the underlying iconify name so each SVG body is emitted once.
const byIcon = new Map();
for (const [slug, ln] of Object.entries(LOG)) {
  if (!byIcon.has(ln)) byIcon.set(ln, []);
  byIcon.get(ln).push(slug);
}

// Sanitize an iconify name into a valid JS identifier for the data const.
const constName = (ln) => '_' + ln.replace(/[^a-zA-Z0-9]/g, '_');

let out =
  HEAD +
  "import type { IconifyIcon } from '@iconify/react';\n\n";

// 1. Emit one const per unique logo.
for (const [ln, _slugs] of byIcon) {
  out += `const ${constName(ln)}: IconifyIcon = ${JSON.stringify(getIcon(lg, ln))};\n`;
}
out += '\n';

// 2. Export the slug → const map. Aliases share the same const instance.
out += '/** Цветные бренд-логотипы (Iconify logos:), инлайн офлайн. slug → данные. */\n';
out += 'export const LOGO_DATA: Record<string, IconifyIcon> = {\n';
for (const [slug, ln] of Object.entries(LOG)) {
  out += `  ${slug}: ${constName(ln)},\n`;
}
out += '};\n';
fs.writeFileSync('src/shared/ui/tech-icon-data.ts', out);

console.log(
  'tech-icon-data.ts:',
  Object.keys(LOG).length,
  'slugs →',
  byIcon.size,
  'unique logos',
);
